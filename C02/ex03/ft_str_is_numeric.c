int	ft_str_is_numeric(char *str)
{
	int aux;
	int i;

	i = 0;
	aux = 1;
	if (str[i] == '\0')
		return (1);
	else
	{
		while (str[i] != '\0')
		{
			if (!(str[i] >= 48 && str[i] <= 57))
				aux = 0;
			i++;
		}
	}
	return (aux);
}
