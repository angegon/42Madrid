#include "rush01.h"

void	ft_conditionsone(int **tab)
{
	if ((tab[0][1] == 1 && tab[5][1] == 3) && (tab[2][0] == 3 &&
				tab[2][5] == 1))
		tab[3][1] = 3;
	if ((tab[0][1] == 1 && tab[5][1] == 3) && (tab[3][0] == 3 &&
				tab[3][5] == 1))
		tab[2][1] = 3;
	if ((tab[0][1] == 1 && tab[5][1] == 3) && (tab[2][0] == 2 &&
				tab[2][5] == 3))
		tab[2][1] = 2;
	if ((tab[0][3] == 3 && tab[5][3] == 1) && (tab[2][0] == 3 &&
				tab[2][5] == 1))
		tab[2][1] = 2;
}

void	ft_conditionstwo(int **tab)
{
	if ((tab[0][2] == 1 && tab[5][2] == 3) && (tab[5][0] == 1 &&
				tab[5][5] == 3))
		tab[5][3] = 3;
	if ((tab[0][5] == 3 && tab[5][5] == 1) && (tab[3][0] == 1 &&
				tab[3][5] == 3))
		tab[2][5] = 3;
	if ((tab[1][0] == 3 && tab[5][0] == 1) && (tab[0][3] == 3 &&
				tab[5][3] == 1))
		tab[1][2] = 2;
}
