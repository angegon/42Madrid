int	ft_is_prime(int nb)
{
	int		aux;

	aux = 2;
	if (nb < 0 || nb == 1 || nb == 0)
		return (0);
	if (nb == 3)
		return (1);
	while (aux < nb)
	{
		if (nb % aux == 0)
			return (0);
		aux++;
	}
	return (1);
}
