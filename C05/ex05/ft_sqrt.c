int		ft_sqrt(int nb)
{
	int z;

	z = 1;
	if (nb < 0)
		return (0);
	while ((z * z <= nb) && (z < 46341))
	{
		if (z * z == nb)
			return (z);
		if (z * z > nb)
			return (0);
		z++;
	}
	return (0);
}
